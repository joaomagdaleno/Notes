/// Represents the different editing personas of the editor.
enum EditorPersona {
  /// Modular/Linear focus (Notion-style). Default.
  architect,

  /// Page layout focus (Word/Docs-style).
  writer,

  /// Free-form canvas focus (Samsung Notes/Whiteboard-style).
  brainstorm,

  /// Reading mode (Adobe Acrobat/Google Play Books-style).
  reading,
}
